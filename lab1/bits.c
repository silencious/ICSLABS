/* 
 * bitMask - Generate a mask consisting of all 1's 
 *   lowbit and highbit
 *   Examples: bitMask(5,3) = 0x38
 *   Assume 0 <= lowbit <= 31, and 0 <= highbit <= 31
 *   If lowbit > highbit, then mask should be all 0's
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int bitMask(int highbit, int lowbit) {
  /* use shift to get bits over lowbit and highbit set 1*/
  int m1=(~0)<<lowbit;
  int m2=(~0)<<highbit<<1;
  return (m1^m2)&m1;
}
/* 
 * bitXor - x^y using only ~ and & 
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 2
 */
int bitXor(int x, int y) {
  /* pick the bits when x and y are the same, combine their opposite*/
  return ( ~(x & y) ) & ~( (~x) & (~y) );
}
/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
  /* if x is true, then !x=0, !!x=1, else x=0, !x=1. get Tmin from 1. */
  int a=((!x)<<31)>>31;
  int b=((!!x)<<31)>>31;
  return (b & y) | (a & z);
}
